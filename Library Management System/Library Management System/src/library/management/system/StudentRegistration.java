    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
     if(textid.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textid.requestFocus();
     }   
     
     else if(textname.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textname.requestFocus();
     }
      else if(textcourse.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textcourse.requestFocus();
     }
      else if(textsemester.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textsemester.requestFocus();
     }
      else if(textsection.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textsection.requestFocus();
     }
      else if(textemail.getText().equals("")){
         JOptionPane.showMessageDialog(this, "All field is Required");
         textemail.requestFocus();
     }
     
      else{
         try {
             Connection con=connect.connection();
             PreparedStatement pat=con.prepareStatement("Insert Into Student(id,name,course,semester,section,email)values(?,?,?,?,?,?)");
             pat.setString(1, textid.getText());
             pat.setString(2, textname.getText());
             pat.setString(3, textcourse.getText());
             pat.setString(4, textsemester.getText());
             pat.setString(5, textsection.getText());
             pat.setString(6, textemail.getText());
             pat.executeUpdate();
             JOptionPane.showMessageDialog(this, "Record Saved");
             textid.setText("");
             textname.setText("");
             textcourse.setText("");
             textsemester.setText("");
             textsection.setText("");
         } catch (SQLException ex) {
             Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
         }
      }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnsaveActionPerformed

